type Lang = keyof typeof translations;
type Keys = keyof typeof translations.en;

export function t(key: Keys): string {
	const lang = userLocale.lang as Lang;
	const dict = translations[lang] ?? translations.en;
	const val = (dict as Record<string, unknown>)[key] ?? translations.en[key];
	return typeof val === 'string' ? val : (translations.en[key] as string);
}

export function tFn(key: Keys): (...args: [number, number]) => string {
	const lang = userLocale.lang as Lang;
	const dict = translations[lang] ?? translations.en;
	const val = (dict as Record<string, unknown>)[key] ?? translations.en[key];
	return typeof val === 'function'
		? (val as (...args: [number, number]) => string)
		: (translations.en[key] as (...args: [number, number]) => string);
}
